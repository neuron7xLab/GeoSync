# ---- T8: claim-leakage trap --------------------------------------


def test_p3_trap_claim_leakage_rejected() -> None:
    """Module surface MUST NOT carry a D-002G PASS claim literal."""
    from research.systemic_risk import d002g_null_mechanisms as mod

    # Inspect public surface
    for name in mod.__all__:
        obj = getattr(mod, name)
        s = repr(obj)
        # Banned strings: any active scientific PASS claim
        banned = (
            "D-002G validated",
            "scientific result confirmed",
            "M2 proves",
        )
        for phrase in banned:
            assert phrase not in s, f"banned claim in {name}: {phrase!r}"
