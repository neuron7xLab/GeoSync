# ---- T7: block-semantics mutation rejected ------------------------


def test_p3_trap_block_semantics_mutation_rejected() -> None:
    """block_structured: node-id permutation across blocks IS topology
    mutation, even though the K_baseline mask is fully-connected."""
    sub = SUBSTRATE_BY_ID["block_structured"]
