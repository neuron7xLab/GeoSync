# Contract fail-closed + PE guard
# ---------------------------------------------------------------------------


def test_integral_contract_fail_closed() -> None:
    """Integral estimator rejects contract violations (no silent repair)."""
    traj = _swing_traj(
        np.array([[0.0, 0.5], [0.5, 0.0]]),
        np.array([0.1, -0.1]),
        np.array([0.4, 0.4]),
        np.array([0.2, 0.2]),
        dt=0.01,
        n=400,
        theta0=np.array([0.3, -0.3]),
    )
    w = np.mod(traj, 2.0 * np.pi)
    w = np.clip(w, 0.0, np.nextafter(2.0 * np.pi, 0.0))
    pm = PhaseMatrix(
        theta=w,
        timestamps=np.arange(w.shape[0], dtype=np.float64) * 0.01,
        asset_ids=("a", "b"),
        extraction_method="hilbert",
        frequency_band=(1e-6, 0.5),
    )
    with pytest.raises(ValueError, match="dt"):
        estimate_swing_coupling_integral(pm, np.ones(2), np.full(2, 0.2), dt=0.0)
    with pytest.raises(ValueError, match="inertia"):
        estimate_swing_coupling_integral(pm, np.zeros(2), np.full(2, 0.2), dt=0.01)
    with pytest.raises(ValueError, match="shape"):
        estimate_swing_coupling_integral(pm, np.ones(3), np.full(3, 0.2), dt=0.01)
    with pytest.raises(ValueError, match="bump_order"):
        estimate_swing_coupling_integral(pm, np.ones(2), np.full(2, 0.2), dt=0.01, bump_order=1)
    with pytest.raises(ValueError, match="test_support"):
        estimate_swing_coupling_integral(pm, np.ones(2), np.full(2, 0.2), dt=0.01, test_support=3)


def test_integral_pe_guard_fires_on_phase_locked() -> None:
    """A rigidly-rotating cluster trips the PE guard (fail-closed)."""
    t_len = 1200
    phi = np.array([0.0, 0.4, -0.3], dtype=np.float64)
    t = np.arange(t_len, dtype=np.float64) * 0.01
    traj = phi[None, :] + 0.2 * t[:, None]
    pm = _pm(traj, 0.01)
    with pytest.raises(PersistentExcitationError):
        estimate_swing_coupling_integral(
            pm,
            np.ones(3),
            np.full(3, 0.2),
            dt=0.01,
            test_support=120,
            n_windows=120,
            bump_order=6,
            pe_min_singular_ratio=PE_HARD_FLOOR,
            pe_guard=True,
        )
    # Guard disabled: diagnostic still reported, no silent crash.
    est = estimate_swing_coupling_integral(
        pm,
        np.ones(3),
        np.full(3, 0.2),
        dt=0.01,
        test_support=120,
        n_windows=120,
        bump_order=6,
        pe_guard=False,
    )
    assert est.min_singular_ratio < 1e-3
